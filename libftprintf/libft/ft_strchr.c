#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*pt;

	i = 0;
	pt = NULL;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i++;
	}
	if ((char)c == '\0')
	{
		return ((char *)(s + i));
	}	
	return (pt);
}
